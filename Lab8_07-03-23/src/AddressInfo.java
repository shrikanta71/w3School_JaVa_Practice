public class AddressInfo
{
    public String street;
    public String city;
    public String state;
    public String postalCode;

    public AddressInfo(String street, String city, String state, String postalCode)
    {
        this.street = street;
        this.city = city;
        this.state = state;
        this.postalCode = postalCode;
    }
}
