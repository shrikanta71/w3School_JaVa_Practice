public class Basics
{
    public static void main(String[] args)
    {
        System.out.println("\nThis is my first code in JAVA language.");


        // Data-type and variables in Java.
        int myNum = 5;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = false;
        String myText = "Hello";

        System.out.println("\n\nMy integer number: " + myNum + "\nMy floating number: " + myFloatNum);
        System.out.println("This is " + myText +  " " + myBool + " " + myLetter);


        // Character or ASCII values in java.
        char var1 = 65, var2 = 97, var3 = 49;

        System.out.println("\n\nThe ASCII value of 65 is: " + var1 + "\nThe ASCII value of 97 is: " + var2 + "\nThe ASCII value of 155 is: " + var3);

    }
}
